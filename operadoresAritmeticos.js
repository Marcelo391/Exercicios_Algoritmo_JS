/* ------  Operadores Aritméticos ------ */

var a = 10;
var b = a + 2; // b = 12s
var c = a - 15; // c = -5
var d = a * 3; // d = 30
var e = a / 3; // e = 3.3333

console.log(`A: ${a}`);
console.log(`B: ${b}`);
console.log(`C: ${c}`);
console.log(`D: ${d}`);
console.log(`E: ${e}`);

/* ------  Outros Operadores Aritméticos ------ */
